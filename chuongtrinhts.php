<p>I. Chương trình đào tạo (CTĐT) trình độ Tiến sĩ:</p>

<ol>
 <li>Nhằm giúp NCS hoàn chỉnh và nâng cao kiến thức cơ bản, có hiểu biết sâu về kiến thức chuyên ngành, có kiến thức rộng về các ngành liên quan, các kiến thức có tính ứng dụng của ngành/chuyên ngành.</li>
 <li>Hỗ trợ NCS tự rèn luyện khả năng nghiên cứu, khả năng xác định vấn đề và độc lập giải quyết các vấn đề thuộc lĩnh vực chuyên môn, khả năng thực hành cần thiết.</li>
 <li>Hỗ trợ NCS tự rèn luyện phương pháp nghiên nghiên cứu, phương pháp viết các bài báo cáo khoa học và phương pháp trình bày kết quả nghiên cứu của mình.</li>
</ol>

<p>II. CTĐT trình độ TS được thực hiện chủ yếu bằng tự học, tự nghiên cứu dưới sự hướng dẫn của nhà giáo, nhà khoa học, coi trọng rèn luyện thói quen nghiên cứu khoa học, phát triển tư duy sáng tạo trong phát hiện, giải quyết những vấn đề chuyên môn.</p>

<p>III. CTĐT trình độ TS gồm ba phần:</p>

<ol style="list-style-type:lower-alpha;">
 <li>Phần 1: Các HP bổ sung, .</li>
 <li>Phần 2: Các HP Tiến sĩ, các CĐTS và TLTQ.</li>
 <li>Phần 3: NC khoa học và LATS.</li>
</ol>

<p style="margin-left:21.3pt;">CTĐT trình độ TS có khối lượng và yêu cầu quy định tại các Điều 16, 17, 18, 19 và 20 của Quy định về tổ chức và quản lý đào tạo tiến sĩ&nbsp;Trường Đại học Thủy lợi.</p>

